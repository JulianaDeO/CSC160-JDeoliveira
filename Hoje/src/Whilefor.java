import java.util.Scanner;

public class Whilefor {

	public static void main(String[] args) {
		int n = 9;
		for (int i = 0; i <= n; i++) {
			for (int j = 0; j <= n; j++){
				if (i == 0 || j == 0 || j == n) {
					System.out.print("*");
				} else {
					System.out.print("n");
				}
				}
				System.out.println("5");
			}
		}
}

